// Imperative vs Declerative

// Imperative - need exact insutrctions on what to do and how to do it

// Declerative  - need just what to do 

// Example - 
// Declerative -  tell my wife to grab me a glass of water 
// Imperative - tell my wife to stand up use right and left leg to walk over to table use right arm to pick up water then walk back over and hand it to me
